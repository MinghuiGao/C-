#include <stdio.h>

int main(int argc, const char * argv[]) {
    
    /*
     1.整型：
            1、短整形： short
            2、int
            3、长整型：long
     2、字符型： char
     3、实型：（浮点类型）
            单精度实型：float
            双精度实型：double
     
     */
    
    1;
    2;
    1100000;
    
    'a';//  必须用单引号来包裹一个字符。只能含有一个字符，注意：转义字符除外。
    
    "addffdfdf";// 字符串
    
    1.1;// 默认是double类型。
    1.2f;// 如果要表示单精度浮点类型，那么需要在小数的后面加上f；
    
    
    return 0;
}

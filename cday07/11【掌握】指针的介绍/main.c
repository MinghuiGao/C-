/*
 指针的介绍
    指针就是地址，指针变量，就是一个存放地址的变量，64bit的环境中，占8个字节。
    
    指针的好处：
        1、可以通过地址去访问变量，更灵活
        2、函数可以通过指针返回多个值
        3、在某些过程中提高程序的效率，比如传值的时候。
        4、为复杂的数据结构提供支持。链表，树。
    
    变量的访问方式：
        通过指针来访问变量叫做： 间接访问。
        通过变量名来访问变量叫做： 直接访问。
 
 */
